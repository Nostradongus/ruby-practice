# Creating and using variables

## variable naming convention --> underscore to separate multiple names
character_name = "Macky"
character_age = "35"

## you can also put a parenthesis to enclose the string
# e.g. puts ("Hello " + character_name + "!")
puts "There once was a man named " + character_name
puts "he was " + character_age + " years old."

## changing the value of character_name halfway through
character_name = "Tom"

puts "He really liked the name " + character_name
puts "but didn't like being " + character_age + "."